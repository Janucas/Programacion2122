package ejerciciosBoletin;

public class Ej10CienAlUnoFOR {

	public static void main(String[] args) {
		System.out.println(CienAlUno());
	}
	
	public static int CienAlUno() {
		int i;
		for (i=100;i>0;i--) {
			System.out.println(i);
		}
		return i;
	}

}
